    misclass = 1 - accuracy

    if cmap is None:
        cmap = plt.get_cmap('Paired')

    plt.figure()
    plt.imshow(cm, interpolation='nearest', cmap=cmap)
    plt.tick_params(direction = 'out')
    v1 = np.arange(cm.min(), cm.max()+1, 1)
    plt.colorbar(ticks = v1)

